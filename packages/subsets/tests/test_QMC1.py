def test_QMC1():
    seed(123)
    for n in range(1, 12):
        P = DenseSet(n)
        for i in range(1000):
            P.set(randrange(2**n))

            if i < 10 or i % 10 == 0:
                S = Quine_McCluskey_Step1(P)

                Sa = {}
                for a, u in S:
                    Sa.setdefault(a, []).append(u)

                print(len(S), len(Sa))
                for a in Sa:
                    # prec(u)
                    U = DenseSet(n)
                    for u in Sa[a]:
                        U.set(a)
                    # a + prec(u)
                    U.Not(a)
                    assert U <= P


